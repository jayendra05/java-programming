public class Class_Attributes {
    int x;

    public static void main(String[] args) {
        Class_Attributes cls =new Class_Attributes();
        cls.x=9;
        System.out.println("The value of x is : "+cls.x);
        cls.x=4;
        System.out.println("The value of x is : "+cls.x);


    }
    
}
